    public int getGiro(){
        int g = 0;
        
        if(giroHor.isSelected()){
            g = 0;
        }
        if(giroHorInvert.isSelected()){
            g = 180;
        }
        if(giroVert.isSelected()){
            g = 90;
        }
        if(giroVertInvert.isSelected()){
            g = 270;
        }
        return g;
    }
